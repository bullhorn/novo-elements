/**
 * Get countries
 */
export function getCountries() {
    return COUNTRIES.map(c => c.name);
}

/**
 * Get country by id
 */
export function findByCountryId(id) {
    return COUNTRIES.find(c => c.id === id);
}

/**
 * Find country object by name
 */
export function findByCountryName(name) {
    return COUNTRIES.find(c => c.name === name.trim());
}

/**
 * Find country object by code
 */
export function findByCountryCode(code) {
    return COUNTRIES.find(c => c.code === code.trim());
}

/**
 * Get states by country name
 */
export function getStates(name) {
    let country = COUNTRIES.find(c => c.name === name.trim());
    return country ? country.states.map(s => s.name) : [];
}

/**
 * Get states by country name
 */
export function getStateObjects(name) {
    let country = COUNTRIES.find(c => c.name === name.trim());
    return country ? country.states : [];
}
